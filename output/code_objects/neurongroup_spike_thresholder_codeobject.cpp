void _run_neurongroup_spike_thresholder_codeobject()
{
    using namespace brian;


    ///// CONSTANTS ///////////
    const int64_t N = 400;
const size_t _num_spikespace = 401;
const size_t _numlastspike = 400;
const size_t _numnot_refractory = 400;
const size_t _numt = 1;
const size_t _numtheta = 400;
const size_t _numv = 400;
const double v_offset_exc = 0.02;
const double v_threshold_exc = - 0.052000000000000005;
    ///// POINTERS ////////////
        
    int32_t* __restrict  _ptr_array_neurongroup__spikespace = _array_neurongroup__spikespace;
    double* __restrict  _ptr_array_neurongroup_lastspike = _array_neurongroup_lastspike;
    char* __restrict  _ptr_array_neurongroup_not_refractory = _array_neurongroup_not_refractory;
    double*   _ptr_array_defaultclock_t = _array_defaultclock_t;
    double* __restrict  _ptr_array_neurongroup_theta = _array_neurongroup_theta;
    double* __restrict  _ptr_array_neurongroup_v = _array_neurongroup_v;



    //// MAIN CODE ////////////
    // scalar code
    const int _vectorisation_idx = -1;
        
    const double _lio_1 = v_threshold_exc - v_offset_exc;



    long _count = 0;
    for(size_t _idx=0; _idx<N; _idx++)
    {
        const size_t _vectorisation_idx = _idx;
                
        const char not_refractory = _ptr_array_neurongroup_not_refractory[_idx];
        const double theta = _ptr_array_neurongroup_theta[_idx];
        const double v = _ptr_array_neurongroup_v[_idx];
        char _cond;
        if(!not_refractory)
            _cond = (v > (_lio_1 + theta)) && false;
        else 
            _cond = (v > (_lio_1 + theta)) && true;

        if(_cond) {
            _ptr_array_neurongroup__spikespace[_count++] = _idx;
            _ptr_array_neurongroup_not_refractory[_idx] = false;
            _ptr_array_neurongroup_lastspike[_idx] = _ptr_array_defaultclock_t[0];
        }
    }
    _ptr_array_neurongroup__spikespace[N] = _count;

}


